package ex_010_For_Loop;

public class Lab096_For_loop_concept {
    public static void main(String[] args) {
        for (long i = 1l; i < 10 ; i++)
        {
            System.out.println(i);
        }
        for (float f = 0.0f; f <  10.67; f++)
        {
            System.out.println("Hi, Float -> " +f);
        }
    }
}
