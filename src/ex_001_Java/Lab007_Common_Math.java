package ex_001_Java;

public class Lab007_Common_Math {
    public static void main(String[] args) {
        System.out.println(2+2); // 4
        System.out.println(2*2); // 4
        System.out.println(2/2); // 1
        System.out.println(2-2); // 0
        System.out.println("2+2"); //2+2
        System.out.println(3/2); // output 1
    }
}
